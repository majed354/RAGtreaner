def repair_row(row: dict[str, Any], contexts: dict[str, dict[str, Any]]) -> dict[str, Any]:
    benchmark_id = str(row.get("benchmark_id", ""))
    mode = str(row.get("mode", ""))
    question = str(row.get("question", "")).strip()
    context = str(contexts.get(benchmark_id, {}).get("context", "")).strip()
    answer = str(row.get("answer", ""))

    if not context:
        if mode == "legal_opinion":
            row["answer"] = build_empty_context_opinion(question)
        elif mode == "legal_memo":
            row["answer"] = build_empty_context_memo(question)
        elif mode == "legal_analysis":
            row["answer"] = build_empty_context_analysis(question)
        row["status"] = "completed"
        row["provider"] = "manual_repair"
        row["teacher_kind"] = "manual_repair"
        row.pop("error", None)
        return row

    if benchmark_id == "seed::refusal_002" and mode == "legal_memo":
        row["answer"] = build_refusal_002_memo(question)
        row["status"] = "completed"
        row["provider"] = "manual_repair"
        row["teacher_kind"] = "manual_repair"
        row.pop("error", None)
        return row

    if benchmark_id == "seed::refusal_002" and mode == "legal_analysis":
        row["answer"] = build_refusal_002_analysis(question)
        row["status"] = "completed"
        row["provider"] = "manual_repair"
        row["teacher_kind"] = "manual_repair"
        row.pop("error", None)
        return row

    if any(pattern in answer for pattern in EMPTY_CONTEXT_PATTERNS) and not context:
        row["status"] = "completed"
        row["provider"] = "manual_repair"
        row["teacher_kind"] = "manual_repair"
        row.pop("error", None)
    return row


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--teachers", type=Path, required=True)
    parser.add_argument("--contexts", type=Path, required=True)
    args = parser.parse_args()

    teachers = load_json(args.teachers)
    contexts = {row["benchmark_id"]: row for row in load_jsonl(args.contexts)}

    repaired_rows = [repair_row(dict(row), contexts) for row in teachers.get("rows", [])]
    teachers["rows"] = repaired_rows
    teachers["summary"] = {
        "examples_total": len(repaired_rows),
        "completed": sum(1 for row in repaired_rows if row.get("status") == "completed"),
        "failed": sum(1 for row in repaired_rows if row.get("status") != "completed"),
    }
    dump_json(args.teachers, teachers)
    print(
        json.dumps(
            teachers["summary"],
            ensure_ascii=False,
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
