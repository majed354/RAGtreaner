"""Run a stratified article-learning diagnostic for the autopilot loop.

The normal autopilot intentionally explores new article pairs, so its
pre-improvement score can stay noisy even when prior gaps are retained.  This
diagnostic separates the question into slices:

* fixed_manual: stable manual cases.
* retained_replay: previously failed autopilot cases after later improvements.
* route_surface_replay: cases where route/diagnostics failed around expected
  article surfaces.
* seen_slug_new_articles: new article pairs under regulations already seen by
  autopilot, generated by the local teacher.

It never mutates the RAG artifacts.  It only writes diagnostic cases/reports.
"""

