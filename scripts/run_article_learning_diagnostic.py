DEFAULT_MANUAL_CASES = ROOT / "data" / "eval" / "manual_article_precision_gate_20260526.jsonl"
DEFAULT_SERVICE_URL = "http://127.0.0.1:8000/internal/rag/query"

if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

